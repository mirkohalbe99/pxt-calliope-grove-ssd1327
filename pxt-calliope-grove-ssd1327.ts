    //% blockId="oled_ssd1327_Speed5Frames" block="5 frames"
    scroll5frames = 0,
    //% blockId="oled_ssd1327_Speed25Frames" block="25 frames"
    scroll25frames = 6,
    //% blockId="oled_ssd1327_Speed64Frames" block="64 frames"
    scroll64frames = 1,
