    //% blockId="oled_ssd1327_setHorizontalMode" block="set OLED to horizontal mode"
    //% parts="oled_ssd1327"
    //% advanced=true
    export function setHorizontalMode() {
        displayCmds([0xa0, 0x42])
        resetAdressing()
    }
